//comparision operator for compare operator
{
    // == comparision (equal to)
       let a=20;
       let b=2;
   console.log("A == B? " ,a==b)
   }

   {
    // != comparision (not equal to)
       let a=20;
       let b=2;
   console.log("A != B? " ,a!=b)
   }

   {
    // === comparision (equal value and type)
       let a=20;
       let b=2;
   console.log("A === B? " ,a===b)
   }

   {
    // !== comparision (not equal value and not equal type)
       let a=20;
       let b=2;
   console.log("A !== B? " ,a!==b)
   }

   {
    // > compare (greater than)
       let a=20;
       let b=2;
   console.log("A > B? " ,a>b)
   }

   {
    // < comparision (less than)
       let a=20;
       let b=2;
   console.log("A < B? " ,a<b)
   }

   {
    // >= comparision (greater than or equal to)
       let a=20;
       let b=2;
   console.log("A >= B? " ,a>=b)
   }
  
   {
    // <= comparision (greater than or equal to)
       let a=20;
       let b=2;
   console.log("A <= B? " ,a<=b)
   }

